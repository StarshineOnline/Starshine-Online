<h1>Architecte</h1>
L'architecte est un personnage important pour un royaume, en effet il a la possibilité d'accélérer la construction des bâtiments de royaume, et même de les réparer !<br />
Il peut aussi construire ou améliorer les terrain et bâtiments de joueurs (lien chantier dans la ville).