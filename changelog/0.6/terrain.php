<h1>Terrain de joueurs</h1>
Il y a un nouveau lien dans la ville vous permettant d'enchérir sur des terrains.<br />
Ils sont mis en vente automatiquement par le royaume le lundi en fonction du nombre de case qu'il possède<br />
Au bout d'une semaine l'enchère est finie est revient au plus offrant.<br />
Sur ce terrain vous pouvez lancer la construction de bâtiments comme un laboratoire, une chambre, un coffre, etc. Chaque bâtiment à un nombre de point de structure (par exemple 10 000), vous devez fixer un nombre de star par point de structure pour que la construction commence. (le minimum étant 1 star / point de structure)<br />
Une fois la construction lancée, les architecte peuvent dans la ville accéder à une page leur permettant de faire avancer la construction. Ils reçoivent le nombre de star que vous avez fixé (moins la taxe) par nombre de point de structure qu'ils ont fait.<br />
Ces bâtiments prennent un nombre de case, et vous pouvez améliorer votre terrain pour qu'il ai plus de cases et ainsi en avoir plus ou de plus grands.<br />

<h1>Liste des bâtiments</h1>
<ul>
	<li>Coffre : Permet de stocker 10 objets. (prend 1 case)</li>
	<li>Coffre II : Permet de stocker 20 objets. (prend 2 cases)</li>
	<li>Coffre III : Permet de stocker 30 objets. (prend 3 cases)</li>
	<li>Laboratoire : Permet de faire de l'alchimie sans utiliser les instruments de la ville (vous pouvez aussi améliorer vos instruments). (prend 2 cases)</li>
	<li>Grenier : Permet de réduire votre famine de 1% pour 10 PA. (prend 1 case)</li>
</ul>