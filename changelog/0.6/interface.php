<h1>Interface</h1>
<h2>Accueil</h2>
La page d'accueil a été totalement refaite, y est regroupé maintenant uniquement les infos importantes pour tout nouveau joueur (présentation du jeu, screenshots, news et création d'un personnage).<br />
Les anciens menus (classement, stats et autres) ont été ajouté à l'intérieur du jeu sous forme de menu et de "popup".<br />
<br />
Tout cela pour permettre au nouveau joueur de comprendre simplement en quoi consiste le jeu, et pour mettre tout ça en valeur avec une interface plus "professionelle".<br />
Autre avantage, toutes les informations importantes sont regroupées dans le jeu, ainsi plus la peine de faire des allez retour entre l'index et leu jeu !
<h1>Le jeu</h1>
Sur l'interface du jeu, tout le haut a été modifié, la barre d'xp est maintenant intégrée autour de la rose des vents, les points shines sont indiqués avec l'étoile en haut a gauche, et le nombre de buff que l'on peut avoir est clairement visible grâce au pentacle et cadenas.
L'intérêt est d'offrir une meilleur visibilité des informations importantes (stars, honneur, buffs, hp, mp, pa et groupe).<br />
Beaucoup d'autres éléments ont été uniformisés, nottament les infobulles au survol de la souris, ou les information s'affichant à droite qui ont un nouveau design plus clair.