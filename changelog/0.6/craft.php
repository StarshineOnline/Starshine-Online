<h1>Le craft</h1>
Le craft est maintenant divisé en 3 parties non exclusives (vous pouvez très bien être alchimiste, architecte et forgeron en même temps) :<br />
- <a href="architecte.php">Architecte</a> : il peut accélérer la construction des bâtiments de royaume, les réparer, et aider le développement de maison des joueurs (augmentation de la taille, fabrication de ce que l'on peut y mettre).<br />
- Forgeron : Il peut slotter les objets. A la base il devait faire d'autres choses mais au dernier moment on a dut retirer ces éléments car ils pouvaient déséquilibrer l'économie des royaumes. Ces éléments apparaitront quand le problème sera résolu ;)<br />
- <a href="alchimie.php">Alchimiste</a> : Il peut faire des potions et parchemins.<br />
<br />
Vu qu'il n'y avait que la fabrication d'objet auparavant, nous avons créer un petit outil pour vous permettre de répartir vos anciens points de craft dans ces 3 branches.<br />
On peut y accéder dans l'onglet "jeu" => menu "craft".<br />
/!\Attention, les points mis ne peuvent plus être modifiés après, venez pas pleurer parce que vous avez cliqué à côté./!\<br />
<br />
Il est a noté aussi que vos nouveaux points de craft sont égaux à (ancien craft)² / 10.