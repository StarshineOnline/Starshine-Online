<h1>Nouvelle Messagerie</h1>
Le fonctionnement de la messagerie a été totalement revu, c'est pourquoi les boîte de message sont vides ^^<br />
Dorénavant, la messagerie marche par topic.<br />
Si vous envoyez un message a un joueur ou à votre groupe, cela créer un topic, si quelqu'un répond le message sera affiché a la suite du topic, etc.<br />
Cela permet une plus grande clarté et lisibilité, et surtout un meilleur suivi des conversations.<br />
Il y a aussi différente options pour rendre les topics importants pour qu'ils soient affichés plus haut dans la liste, ou pour masquer des messages, mais je vous laisse découvrir tout ça.<br />
Attention, pour supprimer un topic, il faut soit être chef de groupe, soit être initiateur d'une conversation entre joueur et qu'il n'y ai plus de message dans la conversation.<br />