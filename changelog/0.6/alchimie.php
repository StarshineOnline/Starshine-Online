<h1>L'alchimie comment ça marche</h1>
Très simple, il vous suffit d'avoir une recette (consultable dans livre -> livre de recette), tous les ingrédients listés pour cette recette, un récipient utilisé pour cette recette, et vous trouver en ville.<br />
Si vous avez tous ces éléments, à côté du nom de votre recette sera indiqué "Fabriquer X PA / X MP / X stars".<br />
Si vous avez assez de PA / MP / Stars cliquez dessus et cela vous affiche un petit formulaire vous demandant quel type de récipient vous voulez utiliser.<br />
Choisissez en un, et voila, la recette a été fabriquée !<br />

<h1>Comment avoir des recette ?</h1>
Rendez vous chez votre alchimiste en ville !<br />
Une liste de recette vous y attendra. (si il n'y en a pas, vous référer à "A quoi servent les étude en alchimie ?").<br />

<h1>Comment avoir les ingrédients ?</h1>

La plupart se trouvent en tuant des monstres. Mais si vous n'aimez pas la bagarre, on en trouve souvent via l'hôtel des ventes !<br />

<h1>Comment avoir un récipient ?</h1>
Ils sont en vente chez votre alchimiste !<br />

<h1>A quoi servent les récipients ?</h1>
Les recettes utilisent des récipients différents, et chaque récipient défini les PA / MP qui seront dépensé à l'utilisation de l'objet fabriqué.<br />
Par exemple, une potion de vie mineure fabriquée avec une fiole éclair coutera 0 PA et 20 Mp pour être utilisée (et redonnera 40 HP), tandis que la potion de vie mineure fabriquée avec une fiole a bouchon coutera 5 PA et 5 MP pour être utilisée (et redonnera aussi 40 HP).<br />

<h1>J'ai vu une liste d'instrument pour chaque recette, quoi que c'est ?</h1>
Il y a 3 familles d'instruments :<br />
- Les mortiers<br />
- Les fours<br />
- Les cornues<br />
Chaque recette demande un ou plusieurs instruments différents.<br />
<br />
Par défaut ceux de la ville seront utilisés. Mais ils ont 2 gros défauts :<br />
- Ce sont les plus basiques<br />
- Ils coutent de l'argent a être utilisés<br />
<br />
Pour remédier à cela, vous pouvez construire sur votre terrain (si vous en avez un) un laboratoire.<br />
Une fois construit, achetez les instruments que vous voulez. Et vous pouvez même les améliorer !<br />
Avoir ses propres instrument a 2 avantages :<br />
- Ils ne vous coute plus d'argent à chaque utilisation<br />
- Ils peuvent être amélioré et donc réduire le coût en PA et MP pour fabriquer la recette !<br />

<h1>A quoi servent les études en alchimie ?</h1>
Cela permet de faire monter le savoir en alchimie de votre royaume.<br />
Plus le savoir en alchimie est élevé, plus il y aura de recette disponibles chez l'alchimiste !<br />