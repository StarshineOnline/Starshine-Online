	<h2 id="Gestion-du-royaume">Gestion du royaume<a href="#Gestion-du-royaume" class="wiki-anchor"></a></h2>


	<h3 id="Gestion-des-bâtiments-en-ville">Gestion des bâtiments en ville.<a href="#Gestion-des-bâtiments-en-ville" class="wiki-anchor"></a></h3>


	<p>Il est maintenant possible de "downgrader" ou réparer un bâtiment en ville, la réparation coûte le prix de l'upgrade multipliée par le pourcentage de vie restante.<br />Par contre chaque action demandera un temps d'inactivité sur ce bâtiment pour être faite.<br />Réparer exige que le bâtiment n'a pas eu d'action (réparation / upgrade / downgrade) les 3 derniers jours.<br />Downgrade exige 1 semaine par différence de niveau.<br />Upgrade exige 1 mois par différence de niveau.</p>


	<p>Il est à noter que l'on ne peut pas upgrader un bâtiment abimé.</p>


	<h3 id="RvR">RvR<a href="#RvR" class="wiki-anchor"></a></h3>


	<p>On ne peut pas supprimer une construction qui a moins de 10% de ses PV.<br />Si un royaume attaque une ville, et que la ville possède moins de 2000 ressources au total, l'attaquant ne pourra plus supprimé de ressources et attaquera donc autre chose automatiquement.</p>


