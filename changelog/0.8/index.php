	<h2 id="Rangs-4">Rangs 4<a href="#Rangs-4" class="wiki-anchor"></a></h2>


	<p>Les rangs 4 permettent maintenant d'obtenir un sort unique à sa classe dont voici la liste :<br />- Elémentaliste => Retour en ville (groupe) => TP de groupe vers la ville. (35 MP / 25 PA).<br />- Sage => Faveur divine => redonne 5% des PV. (30 MP / 4 PA)<br />- Pestimancien => Contagion => (buff) Réduit le coût en PA de 2, MP de 15 des sorts de maladies. (20MP / 4PA / durée 1 jour)<br />- Titan => Pour la gloire => (buff de groupe) Augmente le gain en honneur de 15%. (50MP / 4PA / durée 1 semaine)<br />- Ombre => Sabotage => (cible => arme de siège) Empêche le soin et / ou l'accélération de construction. (30 MP / 5 PA / durée 1 jour)<br />- Sniper => Charisme => (Buff de groupe qui ne comptera pas dans le nb de buff) permettant d'avoir un buff supplémentaire. (30MP / 5 PA / durée 1 semaine)<br />- Templier => Balance => Nivelle tous les points de vie des joueurs du groupe. (on fait le total des % de PV du groupe, et on divise par le nombre de membre pour avoir les PV en % de chaque joueur). (30MP / 5PA)<br />- Conjurateur => Invocation d'un élémentaire. (150 MP / 10 PA)<br />- Druide Ollamh => Invocation d'une bestiole genre ange. (150 MP / 10 PA)<br />- Démoniste => Invocation d'un démon. (150 MP / 10 PA)<br />- Dresseur => Invocation d'une grosse bête. (150 MP / 10 PA)<br />- Derviche => Libération => (self) Supprime un buff, et vous redonne 7% de vos PV. (15 MP / 5 PA)<br />- Prédateur => Redonne 20 MP. (30 MP / 2 PA)</p>


