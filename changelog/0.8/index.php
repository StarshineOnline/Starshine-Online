	<h2 id="Dressage">Dressage<a href="#Dressage" class="wiki-anchor"></a></h2>


	<h3 id="Comment-se-passe-le-dressage-">Comment se passe le dressage :<a href="#Comment-se-passe-le-dressage-" class="wiki-anchor"></a></h3>

	<p>On se positionne sur une case, on clique sur un mob de cette case, et on clique sur dresser.<br />Cela va vous lancer un buff d'une durée de 2 jours.<br />Pendant que ce buff est actif, il est impossible de se déplacer ou attaquer. Si vous vous faîtes attaquer, le buff est supprimé.<br />Durant le premier jour du buff, vous pouvez utiliser votre compétence dressage, pour augmenter vos chances de dresser la créature.<br />Au bout d'un jour, il vous est possible de tenter le dressage de la créature, si vous réussissez, elle vient avec vous.</p>


	<h3 id="A-quoi-sert-la-créature-">A quoi sert la créature ?<a href="#A-quoi-sert-la-créature-" class="wiki-anchor"></a></h3>


	<p>La créature ainsi dressée vous permet d'attaquer a votre place (le script de combat est non modifiable), ou de défendre si vous avez le buff adéquat.<br />Certaines créatures peuvent aussi utiliser des sorts.</p>


	<h3 id="Je-vais-avoir-trop-de-créature-comment-je-la-range-dans-ma-boîte-pokemon-">Je vais avoir trop de créature, comment je la range ? dans ma boîte pokemon ?<a href="#Je-vais-avoir-trop-de-créature-comment-je-la-range-dans-ma-boîte-pokemon-" class="wiki-anchor"></a></h3>


	<p>En ville sera disponible une écurie, payante (lors de la dépose de la bestiole), avec une limite de 10 créatures.<br />Mais il sera possible si vous avez une maison de construire une écurie et de pouvoir avoir par exemple un stockage de 25 bestioles.</p>


	<h3 id="Comment-défendre-avec-ma-créature-">Comment défendre avec ma créature ?<a href="#Comment-défendre-avec-ma-créature-" class="wiki-anchor"></a></h3>

	<p>Au passage de rang pour les classes typées dressage, une compétence vous est donné vous donnant une chance de défendre avec votre créature principale.<br />Rang 2 => 30% de chance de défendre<br />Rang 3 => 45% de chance de défendre<br />Rang 4 => 55% de chance de défendre</p>


	<h3 id="Combien-je-peux-avoir-de-créature-avec-moi-">Combien je peux avoir de créature avec moi ?<a href="#Combien-je-peux-avoir-de-créature-avec-moi-" class="wiki-anchor"></a></h3>


	<p>Cela dépend de votre rang :<br />Rang 1 => 1 créature<br />Rang 2 => 2 créature<br />Rang 3 => 3 créature<br />Rang 4 => 4 créature</p>


