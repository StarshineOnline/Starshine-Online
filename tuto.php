<div class="titre" id="inter">Interface</div>
<p>
A votre permière connexion, vous arrivez sur la ville de votre royaume.<br />
Votre personnage se trouve au centre de la carte, cliquez donc sur le centre de la carte.<br />

Voici une brève explication de l'interface <img src="image/premiereaide.jpg" />
</p>


<h3>1 - La Carte du monde</h3>
<p>
Cette petite carte de 7 * 7 cases représente le monde qui vous entoure, chaque case à une coordonnée X et Y qui vous permettra de vous situer dans le monde global.<br />
Votre position est indiquée par les coordonnées en rouge, et vous devez vous trouver au centre de la carte, representé par un petit personnage symbolisant votre race.<br />
Tout autour de vous, vous pouvez distinguer les différents types de terrains (ville, plaine, forêt, etc.), les autres joueurs (symbolisés par des petits personnages de leur race), ainsi que les monstres alentours.<br />
Il ne peut y avoir de monstres sur la ville.<br />
En laissant votre souris sur une case, vous pouvez avoir plus d'informations sur les joueurs et monstres qui sont sur cette case.<br />
Pour avoir encore de plus amples informations sur la case, cliquez dessus et à droite s'affichera de nouvelles informations que l'on verra plus en détails plus tard.
</p>
<h3>2 - La rose de déplacement</h3>
Pour vous déplacer dans le monde de Starshine, rien de plus simple, utilisez la petite boussole située en haut a gauche, le coût de PA en déplacement est indiqué lorsque vous cliquez sur une case dans les informations de celle-ci. Notez qu'un déplacement en diagonale vous coûtera un PA de plus qu'indiqué sur la case.<br />
Autre chose importante de cette boussole, en cliquant au milieu de celle-ci, vous afficherez automatiquement la carte, pratique lorsque vous avez fini de naviguez dans les menus de la ville. A noter que pour attaquer un monstre ou un joueur, vous devez être sur sa case (sauf si vous possédez une baguette ou un arc)

<h3>3 - Récapitulatif du personnage</h3>
<p>
Cette partie est divisée en 3 rectangles et regroupe toutes les informations interessantes de votre personnage.
</p>
<ul style="padding-left : 5px; padding-right : 5px;">
	<li>Le premier rectangle récapitule votre nom, votre race, votre classe et votre niveau.</li>
	<li>Le deuxième rectangle possède 3 barres de couleurs différentes</li>
	<ul>
		<li>La première barre verte symbolise vos points d'action (PA). Ce sont les points qui vous permettent de jouer. Avec vos points d'action, vous pouvez vous déplacer, attaquer un joueur ou un monstre. Vous avez globalement 200 points d'action par jours, que vous regagnez 1 fois toute les 6 minutes. Veillez à ne jamais être à 200 points, car vous ne pouvez pas en gagner plus.</li>
     	<li>La deuxième barre rouge symbolise vos points de vie(HP). Si vos points de vie tombent à 0, vous mourrez. Vous devrez donc ressuciter. Vous ne perdez rien lors de votre mort.<li>
     	<li>La troisième barre bleue symbolise vos points de mana (MP). Ces points vous servent à lancer des sorts sur votre personnage, mais aussi sur les personnage alliés ou ennemis. Vous ne mourrez pas si cette barre tombe à 0, mais vous ne pourrez plus lancer de sorts.<li>
	</ul>
	<li>Le troisième rectangle symbolise vos stars, votre honneur et votre expérience.</li>
	<ul>
		<li>Vos stars (le nom de la monnaie qui circule dans Starshine) représentent l'argent que vous possédez, et vous permettent d'acheter des sorts, des objets, de l'équipement, etc...</li>
     	<li>Votre honneur représente votre réputation. Vous pouvez gagner de l'honneur en accomplissant des quêtes ou en tuant des joueurs ennemis.</li>
     	<li>Votre expérience représente votre niveau. Pour gagner un niveau, vous devez donc accumuler un certain nombre de points d'expérience</li>
	</ul>
</ul>

<h3>4 - Menu du personnage</h3>
<p>
Le menu en haut à droite vous permet de consulter différentes options dont le récapitulatif est disponible ci-dessous : 
</p>
<h4>Menu</h4>
<ul style="padding-left : 5px; padding-right : 5px;">
<li><u>Index :</u> vous permet de revenir à la page d'accueil</li>
<li><u>Diplomatie :</u> vous permet de consulter le tableau diplomatique. Ce tableau représente les relations des races entre elles : alliées (vert), neutres (blanc) ou ennemies (rouge). Seuls les rois peuvent changer la diplomatie entre les races. Il est important de savoir que si vous attaquez un joueur d'une race alliée, vous ne gagnerez rien, mais que si vous attaquez un joueur d'une race ennemie, vous gagnerez plus d'expérience et d'honneur.
Le bas de la page représente les joueurs importants de chaque royaume, ainsi que les constructions disponibles dans chaque ville.</li>
<li><u>Royaume :</u> cette page vous permet de consulter la carte du monde, mais aussi le territoire controlé par chaque race. Le monde de starshine est évolutif, et les joueurs peuvent prendre le controle de territoires en y déposant des drapeaux (représentant la couleur de leur race). Il est donc important de consulter régulièrement cette carte pour voir par quelle race votre royaume est attaqué, ou quelle race votre royaume attaque.</li>
<li><u>Classement :</u> cette page représente le classement des différents joueurs, y aurez vous votre place ?</li>
<li><u>Forum :</u> vous permet d'accéder au forum du jeu. Chaque joueur peut accéder au forum spécifique de sa race. Ce forum est primordial si vous avez des questions, si vous voulez savoir ce qui se passe dans votre royaume. Il est nécessaire de le consulter régulièrement.</li>
<li><u>Ou suis-je :</u> indique la position de votre personnage sur la carte du monde de starshine.</li>
<li><u>Déconnexion :</u> vous déconnecte du jeu (votre personnage reste cependant dans le monde, et peut être attaqué à n'importe quel moment).</li>
</ul>
<h4>Personnage</h4>
<ul style="padding-left : 5px; padding-right : 5px;">
<li><u>Infos persos :</u> représente toute les informations de votre personnage. Il y a 3 parties différentes : </li>
	<ul>
         <li>stats :</u> représente les attributs de votre personnage, et le temps de vos prochaines augmentations. En effet, dans le monde de starshine, votre perso prend de l'age, et ses hp et mp augmenteront régulièrement avec le temps. La regen pa vous indique quand vous gagnerez un point d'action (1 tout les 6 min en moyenne, soit 200 par jours). La regen hp/mp vous indique quand vous regagnerez vos points de vie et mana perdus en combattant (vous n'en regagnerez évidement pas si vos points de vie sont à leur maximum.</li>
         <li>compétence :</u> représente les compétences que vous possédez, et leur évolution symbolisée par la barre verte. Si la barre verte est remplie, cela signifie qe vous ne pourrez plus évoluer dans vos compétences tant que vous n'aurez pas changé de classe. Consulter l'université dans votre ville pour savoir quand vous pourrez changer de classe.</li>
         <li>magie :</u> représente vos compétences magiques (même principe que pour "compétence")</li>
	</ul>
<li><u>Sorts :</u> représente la liste des sorts que vous avez achetés et leur description. L'étoile à droite du sort vous permet de l'ajouter dans vos favoris afin qu'un sort apparaisse directement quand vous cliquez sur "sort".</li>

<li><u>Sorts de combat :</u> représente la liste de vos sorts d'attaque et leur description.</li>

<li><u>Compétences :</u> représente la liste de vos compétences et leur description.</li>

<li><u>Livre de recettes :</u> représente la liste des recettes que vous possédez, ainsi que les ingrédients nécessaires à leur préparation. Vous devez vous rendre à l'alchimiste de votre ville pour pouvoir créer une recette. Les composants nécessaires se trouvent en général sur les monstres que vous tuez.</li>

<li><u>Script de combat :</u> Une des innovations majeures de starshine par rapport aux autres jeux. Une attaque sur un monstre ou un joueur se fait généralement en 5 rounds (excepté orcs). Vous pouvez paramétrer les 5 actions que vous allez faire dans le script de combat, afin de les réaliser à chaque attaque. Il est donc conseillé de créer un script d'attaque, et de créer vos 5 actions en ajoutant les compétences que vous voulez utiliser. Vous pouvez aussi créer un script de défense  qui s'executera automatiquement si vous êtes attaqués par d'autre joueurs. (les compétences que vous pourrez utiliser dans votre script sont limitées par votre réserve de mana)</li>
<p>Exemple : tutotest est troll et possède 9 de réserve de mana, il désire créer un script d'attaque. Tutotest possède la compétence coup puissant qui coute 4 de réserve de mana.<br />
Il clique donc sur : script de combat>créer un script>mode simplifié>nom du script : "attaque"<br />
Ensuite, il crée son script avec ses cxompétences disponibles<br />
</p>
<ul style="padding-left : 5px; padding-right : 5px;">
<li>round1 : attaque (0 réserve utilisée)</li>
<li>round2 : coup puissant (4 réserve de mana utilisée)</li>
<li>round3 : attaque (0 réserve utilisée)</li>
<li>round4 : attaque (0 réserve utilisée)</li>
<li>round5 : coup puissant (4 réserve de mana utilisée)</li>
</ul>
<p>
Il valide chacun des rounds.<br />
Ensuite, il reviens dans son menu de script, et clique sur : choisir le script d'attaque>"attaque"<br />
Voila, maintenant, des que tutotest attaquera un monstre, il effectuera 2 fois coup puissant.<br />
Vous pouvez faire la même chose pour le script de défense.
</p>
<li>journal des actions : récapitule les actions que vous avez effectué, ou que vous avez subies lorsque vous n'étiez pas connecté.</li>
</ul>
