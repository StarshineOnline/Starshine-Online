	<div class="titre" id="jeu">Le jeu</div>
<p>
Vous allez bientôt rentrer dans le fabuleux monde de starshine.<br />
Starshine est un rpg héroic-phantasy, avec des races et classes dont les intrigues politiques les amènent parfois a s'affronter entre elles.<br />
Vous allez incarner un héro d'une race spécifique, qui influera sur le monde selon vos agissements.<br />
Selon la race que vous allez prendre, vous contribuerez à la notoriété de votre royaume gouverné par un roi représenté par un joueur. <br />
Chaque personnage peut jouer en ayant son propre objectif, comme tuer des monstres et rapporter de l'argent à votre royaume, créer des potions et parchemins, tuer les autres joueurs, gagner des titres honorifiques, participer à la diplomatie entre royaumes, servir fidèlement votre roi ...  voir devenir roi !
</p>
