<p>
Plus tard dans le jeu, vous pourrez vous spécialiser en différentes sous-classes dont voici l'arbre futur : <br />
<img src="http://www.mauvaisfond.com/starshine/arbo.jpg" alt="" /><br />
<br />
Choisissez donc bien votre classe en fonction de ce que vous voudrez devenir au final.
<br /><br />
Explication et caractéristiques globales de chaque classe : 
<ul style="padding-left : 5px; padding-right : 5px;">
<li><u>Guerrier :</u> le guerrier est la brute du corps à corps. Il doit être sur la case d'un monstre ou d'un ennemi pour pouvoir l'attaquer. Il est important de bien équiper votre guerrier en lui achetant des armures et de l'équipement. La classe de base pour faire un guerrier est la classe "combattant"</li>
<li><u>Voleur :</u> le voleur est le roi de la sournoiserie. Il est généralement équipé de 2 dagues, 1 dans chaque main, et maitrise à la perfection les coups critiques. La classe de base pour faire un voleur est la classe "combattant"</li>
<li><u>Archer :</u> L'archer est le roi de l'attaque à distance. Il peut attaquer à 1 case de distance s'il est équipé d'un arc. De plus, l'adversaire mettra 1 round à se rapprocher, et donc n'attaquera pas le premier round pendant que vous, vous pourrez attaquer. Il est donc primordial d'acheter un arc très rapidement si vous voulez faire archer. La classe de base pour faire un archer est la classe "combattant"</li>
<li><u>Sorcier :</u> Le sorcier est le roi des sorts de dégats. Il peut attaquer à 1 case de distance s'il est équipé d'une baguette. De plus, l'adversaire mettra 1 round à se rapprocher, et donc n'attaquera pas le premier round pendant que vous, vous pourrez attaquer. Il est donc primordial d'acheter rapidement des sorts de dégats et une baguette si vous voulez faire un sorcier. La classe de base pour faire un sorcier est la classe "magicien".</li>
<li><u>Clerc :</u> Le clerc est le roi des sorts de soins, et des sorts de support. Il est très fort pour augmenter les caractéristiques des autres joueurs, ou pour les soigner. Il possède aussi de nombreux sorts génants pour l'adversaire. Il peut attaquer à 1 case de distance s'il est équipé d'une baguette. De plus, l'adversaire mettra 1 round à se rapprocher, et donc n'attaquera pas le premier round pendant que vous, vous pourrez attaquer. Il est donc primordial d'acheter rapidement des sorts de dégats et des sorts de soin et une baguette si vous voulez faire un clerc. La classe de base pour faire un clerc est la classe "magicien".</li>
</p>
