<h3>Les monstres</h3>
<p>
Quand vous attaquez un monstre, il y aura en bas les points de vie qu'il vous reste, et ses points de vie. Vous pouvez l'attaquer jusqu'à ce qu'il ne vous reste plus de PA, ou que l'un des 2 meurt. Si vous mourrez, vous aurez le choix entre attendre une résurection (mieux ne vaut pas trop y compter si vous n'êtes pas dans un groupe), ou ressuciter à la ville de départ avec 20% hp-mana. C'est la même chose quand vous attaquez un joueur.<br />
Il y a différentes sortes de monstres représentés par un code couleur : 
<li>bleu : le monstre est moins fort que vous. vous gagnerez donc moins d'exp, mais vous le tuerez probablement</li>
<li>blanc : le monstre est de votre niveau, attendez vous à un combat serré</li>
<li>Rouge : Il ne vaut mieux ne pas essayer :)</li>
</p>

<h3>les joueurs</h3>
<p>
Les joueurs peuvent être hostiles, comme amicaux. Regardez bien la race des joueurs environnants, et votre tableau diplomatique, afin de voir s'ils seront amicaux ou non.
</p>

<h3>les groupes</h3>
<p>
Il est parfois utile de grouper entre joueurs, soit pour tuer un gros monstre, soit pour s'entraider, soit pour avoir un groupe polyvalent (clerc+guerrier, archer+mage, etc...). Si vous groupez, vous apprecevrez la barre de vie de votre membre de groupe en haut à droite. L'exp et l'honneur gagné sera partagé équitablement entre les différents membres du groupe. L'objet droppé, s'il y a, sera lui aussi réparti équitablement.
</p>

<h3>monter en niveau</h3>
<p>
Le pvp peut être un bon moyen de gagner honneur et expérience facilement. Cependant, si vous ne faites que ca, vous avez des chances de vous retrouver très vite avec un personnage gros level, mais avec aucune compétence montée, et vous serez une cible très facile pour les autres joueurs. Variez donc vos actions, et n'oubliez pas que réaliser des quetes en tuant des monstres est un bon moyen de progresser.<br />
Quand vous gagnez un niveau, vous aurez le choix entre 3 améliorations : 
<ul style="padding-left : 5px; padding-right : 5px;">
