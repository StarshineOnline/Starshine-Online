<div class="titre" id="tuto">Un léger tutorial pour bien débuter le jeu</div>
<p>
Une fois votre personnage créé (voir le tutorial ci-dessus) en fonction de la classe future que vous voudrez avoir, vous voici devant la ville. <br />
<br />
Si vous avez créé un personnage d'une race minoritaire (ce que j'espere si vous êtes un gars sympa qui veut aider à l'équilibrage :p), vous aurez quelques stars de départ. <br />
Il est temps de rentrer directement dans la ville en cliquant sur "accéder à la ville", et de regarder tout d'abord dans le magasin d'arme, et d'acheter votre permière arme, en fonction des compétences que vous voulez monter (arc pour archer,baguette pour mage, épée ou hache pour guerrier, etc...). Avant d'acheter votre arme, jetez un coup d'oeil a  l'hotel de ventes des joueurs, histoire de voir si vous ne trouvez pas une arme moins chere, ce qui vous permettra de faire des économies.<br />
Cliquez ensuite dans "inventaire" et équipez la. Si vous êtes mage ou archer, vous pourrez maintenant taper les monstres à une case.<br />
Si il vous reste des stars, achetez vous de l'équipement, d'autres sorts, ou gardez vos sous.<br />
Il faut maintenant vous diriger au bureau des quetes, et toute les prendre. Ensuite, dirigez vous à la taverne, et prenez aussi les quetes disponibles.<br />
Suivant votre race, on vous demandera alors d'aller tuer des lapins-scorpions-belettes,etc...<br />
Allez dans votre script de combat, et créez votre script d'action (expliqué plus haut). v
Vous êtes maintenant prets à affronter les monstres alentours. Ca tombe bien, vous avez une quete pour ca. <br />
Foncez donc en dehors de la ville pour trouver un lapin-belette-scorpion, et commencez à l'attaquer....................<br />
</p>		
