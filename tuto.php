<p>
Il vous est tout d'abord demandé de créer un personnage.<br />
Vous aurez le choix parmi 11 races disponibles ayant chacune des aptitudes particulières.(voir récapitulatif des races )<br />
Les caractéristiques de chaque race se présentent ainsi : (lien vers le tableau des races)
