</p>
<ul style="padding-left : 5px; padding-right : 5px;">
<li>L'aventure sera beaucoup plus facile pour vous, car vous pourrez acheter de l'équipement dès le début du jeu</li>
<li>Vous contribuerez à l'équilibre des royaumes, à l'équilibre du jeu, et donc au plaisir de chaucn.</li>
