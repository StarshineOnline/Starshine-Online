<ul style="padding-left : 5px; padding-right : 5px;">
<li><u>Vie :</u> caractérise vos points de vie. Plus le nombre est fort, plus vous aurez de points de vie au départ et lors des augmentations futures</li>
<li><u>Force :</u> représente votre fourchette de dégats et vous permet de porter des armes et armures plus puissantes. Plus vous aurez en force, plus vous ferez mal lors d'attaques physiques (attribut de prédilection pour guerriers)</li>
<li><u>Dextérité :</u> représente votre capacité à toucher votre adversaire, a éviter les coups et a faire des coup critiques. (attribut de prédilection pour archers et assassins)</li>
<li><u>Puissance :</u> représente votre capacité de dégats lors de lancement de sorts d'attaque. (attribut de prédilection pour mages et soutiens)</li>
<li><u>Volonté :</u> représente votre capacité à toucher votre adversaire et a éviter les attaques magiques. </li>
<li><u>Energie :</u> caractérise vos points de mana. Plus le nombre est fort, plus vous aurez de points de mana au départ et lors des augmentations futures. Permet aussi de lancer des soins plus puissants.</li>
</ul>
